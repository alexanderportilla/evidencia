    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
     
         int codigo = Integer.parseInt(txtCodigo.getText());
         int precio = Integer.parseInt(txtPrecio.getText());
         int cantidad = Integer.parseInt(txtCantidad.getText());

        Producto p = new Producto(codigo, txtNombre.getText(), precio, cantidad);
        CProducto controlProducto = new CProducto();
        controlProducto.registrarProducto(p);

        limpiarPantalla();
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
         int cod =Integer.parseInt(txtCodigo.getText());
        CProducto controlProducto=new CProducto();
        controlProducto.pintarTabla (tablaProducto,controlProducto.consultarProductoId(cod));
        
          limpiarPantalla();
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        CProducto controlProducto=new CProducto();
        controlProducto.eliminarProducto(Integer.parseInt(txtCodigo.getText()));
        limpiarPantalla();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int codigo = Integer. parseInt(txtCodigo.getText()); 
        int precio = Integer. parseInt(txtPrecio.getText()); 
        int cantidad= Integer. parseInt(txtCantidad.getText()); 
        
         Producto p=new Producto(codigo, txtNombre.getText(),precio,cantidad);
         
         CProducto controlProducto=new CProducto();
         
         controlProducto.modificarProducto(p);
         limpiarPantalla();
         controlProducto.pintarTabla(tablaProducto, controlProducto.consultarProducto());
    }//GEN-LAST:event_btnModificarActionPerformed

    
